def capital(string):
    '''
    функция принимает строку
    возвращает все заглавные буквы
    '''
    return string.upper()


def capital_letter(string):
    '''
    функция которая делает заглавными
    первые буквы каждого слова в строке,
    поступившей на вход функции
    '''
    return string.title()
